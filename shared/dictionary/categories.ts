// Transform category keys for display (e.g., "bathroomFixture" -> "Bathroom Fixture")
export const categoryWords = transformCategoryKeys(_categoryWords);

// Helper functions
export function getWordCategory(word: string): string | null {
  const wordLower = word.toLowerCase();
  for (const [category, words] of Object.entries(_categoryWords)) {
    if (words.includes(wordLower)) {
      return category;
    }
  }
  return null;
}

export function hasCategory(word: string): boolean {
  return getWordCategory(word) !== null;
}
